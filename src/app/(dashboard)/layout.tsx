const roleLabels: Record<string, string> = {
  super_admin: 'Super Admin',
  reception: 'Reception',
  doctor: 'Doctor',
  lab: 'Lab Technician',
  pharmacy: 'Pharmacist',
  xray: 'Radiologist',
  ultrasound: 'USG Technician',
  accounts: 'Accountant',
};

const roleColors: Record<string, string> = {
  super_admin: 'bg-blue-600',
  reception: 'bg-emerald-600',
  doctor: 'bg-purple-600',
  lab: 'bg-teal-600',
  pharmacy: 'bg-amber-600',
  xray: 'bg-rose-600',
  ultrasound: 'bg-indigo-600',
  accounts: 'bg-cyan-600',
};

export default function AppLayout({ children }: { children: ReactNode }) {
  const pathname = usePathname();
  const router = useRouter();
  const [sidebarOpen, setSidebarOpen] = useState(false);
  const [session, setSession] = useState<{ userId: string; name: string; role: string; department: string } | null>(null);
  const [hospitalName, setHospitalName] = useState('BAGA Hospital');

  useEffect(() => {
    try {
      const s = localStorage.getItem('baga_session');
      if (!s) { router.push('/login'); return; }
      const parsed = JSON.parse(s);
      setSession(parsed);
      const h = getHospital();
      setHospitalName(h.name);
    } catch { router.push('/login'); }
  }, [router]);

  const handleLogout = () => {
    localStorage.removeItem('baga_session');
    router.push('/login');
  };

  if (!session) return null;

  const menuItems = roleMenus[session.role] || roleMenus.reception;
  const currentLabel = menuItems.find(i => {
    const itemBase = i.path.split('?')[0];
    return pathname === itemBase || pathname === i.path;
  })?.label || menuItems.find(i => pathname === i.path)?.label || 'Dashboard';

  return (
    <div className="flex min-h-screen bg-slate-100">
      {sidebarOpen && <div className="fixed inset-0 bg-black/50 z-40 lg:hidden" onClick={() => setSidebarOpen(false)} />}

      {/* Sidebar */}
      <aside className={`sidebar ${sidebarOpen ? 'open' : ''}`}>
        <div className="p-4 border-b border-slate-700">
          <div className="flex items-center gap-3">
            <div className={`w-9 h-9 ${roleColors[session.role] || 'bg-blue-600'} rounded-xl flex items-center justify-center`}>
              <svg className="w-5 h-5 text-white" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M19 21V5a2 2 0 00-2-2H7a2 2 0 00-2 2v16m14 0h2m-2 0h-5m-9 0H3m2 0h5M9 7h1m-1 4h1m4-4h1m-1 4h1m-5 10v-5a1 1 0 011-1h2a1 1 0 011 1v5m-4 0h4" />
              </svg>
            </div>
            <div>
              <h2 className="text-white font-bold text-sm leading-tight">{hospitalName}</h2>
              <p className="text-slate-400 text-xs">{roleLabels[session.role]}</p>
            </div>
          </div>
        </div>

        <nav className="py-3 flex-1">
          <div className="px-4 mb-2">
            <span className="text-xs font-semibold text-slate-500 uppercase tracking-wider">Menu</span>
          </div>
          {menuItems.map((item) => (
            <Link key={item.path} href={item.path} className={`sidebar-link ${(pathname === item.path || (item.path.includes('?') && pathname === item.path.split('?')[0])) ? 'active' : ''}`}
              onClick={() => setSidebarOpen(false)}>
              <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d={item.icon} />
              </svg>
              <span>{item.label}</span>
            </Link>
          ))}
        </nav>

        <div className="absolute bottom-0 left-0 right-0 p-3 border-t border-slate-700">
          <div className="flex items-center gap-2 mb-2">
            <div className={`w-8 h-8 ${roleColors[session.role] || 'bg-blue-600'} rounded-full flex items-center justify-center text-white font-bold text-xs`}>
              {session.name.charAt(0)}
            </div>
            <div className="flex-1 min-w-0">
              <p className="text-white text-sm font-medium truncate">{session.name}</p>
              <p className="text-slate-400 text-xs">{session.department}</p>
            </div>
          </div>
          <button onClick={handleLogout} className="w-full flex items-center gap-2 px-3 py-2 text-red-400 hover:bg-red-500/10 rounded-lg text-sm transition-colors">
            <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1" />
            </svg>
            Logout
          </button>
        </div>
      </aside>

      {/* Main */}
      <div className="main-content flex-1">
        <header className="bg-white border-b border-slate-200 px-6 py-3 flex items-center justify-between sticky top-0 z-30">
          <div className="flex items-center gap-4">
            <button onClick={() => setSidebarOpen(true)} className="lg:hidden p-2 rounded-lg hover:bg-slate-100">
              <svg className="w-5 h-5 text-slate-600" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M4 6h16M4 12h16M4 18h16" />
              </svg>
            </button>
            <h1 className="text-lg font-semibold text-slate-800">{currentLabel}</h1>
          </div>
          <div className="flex items-center gap-3">
            <span className={`badge ${roleColors[session.role] ? roleColors[session.role].replace('bg-', 'bg-') : ''}`} style={{background: 'var(--sidebar-active)', color: 'white'}}>
              {roleLabels[session.role]}
            </span>
          </div>
        </header>
        <main className="p-6">{children}</main>
      </div>
    </div>
  );
}
